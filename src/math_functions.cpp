#include <stdexcept>
#include "..\include/math_functions.hxx"


// 加法函数的实现
int add(int a, int b){
	
    return a + b;
}

// 减法函数的实现
int subtract(int a, int b){
	
    return a - b;
}

// 乘法函数的实现
int multiply(int a, int b){
	
    return a * b;
}

// 除法函数的实现
double divide(double a, double b){
	
    return a / b;
}

// 取模函数的实现
int modulo(int a, int b){
	
    return a % b;
}